function myLogger(msg){

    console.log("On "+ocation+ " Wish You"+msg);

}

switch(ocation,msg){

case 'New Year':
myLogger("Happy New Year");
break;
case 'Birthday':
myLogger("Happy Birthday");
break;
case 'Christmas':
myLogger("Marry christmas");
break;
case 'Anniversary':
myLogger("Happy anniversary");
default:
    myLogger("Good Day!")

}